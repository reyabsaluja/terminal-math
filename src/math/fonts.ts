type FontKind = "bb" | "cal" | "frak" | "bf" | "rm" | "it" | "sf" | "tt";

export function remapLetter(ch: string, font: FontKind, unicode: boolean): string {
  if (!unicode) return ch;
  switch (font) {
    case "bb":
      return BB[ch] ?? ch;
    case "cal":
      return CAL[ch] ?? ch;
    case "frak":
      return FRAK[ch] ?? ch;
    default:
      return ch;
  }
}

